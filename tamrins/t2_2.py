from turtle import *
x = 100
fillcolor('white')
speed(0)
begin_fill()
circle(x, 180)
circle(2*x, 180)
circle(x, -180)
end_fill()
fillcolor('black')
speed(0)
begin_fill()
circle(x, 180)
circle(2*x, 180)
circle(x, -180)
end_fill()
up()
goto(0, 6*x/10)
down()
begin_fill()
circle(x/3)
end_fill()
up()
goto(0, -6*x/10)
down()
fillcolor('white')
begin_fill()
circle(-x/3)
end_fill()
done()